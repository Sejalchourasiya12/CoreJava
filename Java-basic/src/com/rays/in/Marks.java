package com.rays.in;

public class Marks {
	public static void main(String[] args) {
		int mark = 80;
		
		if(mark>=90) {
			System.out.println("Grade A : " +mark);
			
		}else if(mark>=70) {
			System.out.println("Grade B : " +mark);
		}else {
			System.out.println("Grade C : " +mark);
			
		}
		
		
	}

}
