package com.rays.in;

public class StrPracticeA {
  public static void main(String[] args) {
	  
	  String name = "Sejal";
	  System.out.println("String length: " + name.length());
	  
	  System.out.println("String Chartcter: " + name.charAt(0));
	  
	  System.out.println("Index :" +name.indexOf("ejal"));
	  
	  System.out.println("Last Index of :" + name.lastIndexOf("a"));
	  
	  System.out.println("Replace l by u : " +name.replace("l" , "u"));
	  
	  System.out.println("String to lower: " +name.toLowerCase());
	  
	  System.out.println("Striung to upper: " + name.toUpperCase());
	  
	  System.out.println("String start: " +name.startsWith("Sourabh"));
	  
	  System.out.println("String end: " +name.endsWith("al"));
	  
	  System.out.println("Substring:" +name.substring(2));
	
}
	
	
	
}
